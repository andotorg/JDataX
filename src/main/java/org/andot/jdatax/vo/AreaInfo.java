package org.andot.jdatax.vo;

public class AreaInfo {
	private Integer province_Code;
	private String province_Name;
	private Integer city_Code;
	private String city_Name;
	private Integer county_Code;
	private String county_Name;
	private Integer areaFlag;
	public Integer getProvince_Code() {
		return province_Code;
	}
	public void setProvince_Code(Integer province_Code) {
		this.province_Code = province_Code;
	}
	public String getProvince_Name() {
		return province_Name;
	}
	public void setProvince_Name(String province_Name) {
		this.province_Name = province_Name;
	}
	public Integer getCity_Code() {
		return city_Code;
	}
	public void setCity_Code(Integer city_Code) {
		this.city_Code = city_Code;
	}
	public String getCity_Name() {
		return city_Name;
	}
	public void setCity_Name(String city_Name) {
		this.city_Name = city_Name;
	}
	public Integer getCounty_Code() {
		return county_Code;
	}
	public void setCounty_Code(Integer county_Code) {
		this.county_Code = county_Code;
	}
	public String getCounty_Name() {
		return county_Name;
	}
	public void setCounty_Name(String county_Name) {
		this.county_Name = county_Name;
	}
	public Integer getAreaFlag() {
		return areaFlag;
	}
	public void setAreaFlag(Integer areaFlag) {
		this.areaFlag = areaFlag;
	}
	
}
